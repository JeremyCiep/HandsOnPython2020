chaine = "bonjour"

# Deux appels équivalents à "title" (méthode du type str)
print(chaine.title())
print(str.title(chaine))

# Deux appels équivalents à "upper" (méthode du type str)
print(chaine.upper())
print(str.upper(chaine))

